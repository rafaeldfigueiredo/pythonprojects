class Pessoa:
    def __init__(self,nome,sobrenome):
        self.nome = nome
        self.sobrenome = sobrenome

    def criarPessoa():
        return f"{self.nome} {self.sobrenome}"
    
    pessoa = {"nome":self.nome, "sobrenome":self.sobrenome, "criarPessoa":self.criarPessoa}
    
    return pessoa